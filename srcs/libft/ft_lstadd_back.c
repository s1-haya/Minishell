#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*p;

	if (!lst || !new)
		return ;
	p = ft_lstlast(*lst);
	if (p)
		p->next = new;
	else
	{
		*lst = new;
	}
}
