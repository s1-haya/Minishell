char	*ft_strchr(const char *str, int c)
{
	char	*s;

	s = (char *)str;
	if (c < 0)
	{
		c %= 256;
		c += 256;
	}
	if (c > 255)
		c %= 256;
	while (*s != '\0')
	{
		if (*s == c)
			return (s);
		s++;
	}
	if (c == 0)
		return (s);
	return (0);
}
